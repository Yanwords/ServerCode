package com.nju.beans;

public class ScenicSpot {
    private Integer scenicId;
    private String scenicName;
    private String scenicInfo;
    private Float scenicScore;
	public Integer getScenicId() {
		return scenicId;
	}
	public void setScenicId(Integer scenicId) {
		this.scenicId = scenicId;
	}
	public String getScenicName() {
		return scenicName;
	}
	public void setScenicName(String scenicName) {
		this.scenicName = scenicName;
	}
	public String getScenicInfo() {
		return scenicInfo;
	}
	public void setScenicInfo(String scenicInfo) {
		this.scenicInfo = scenicInfo;
	}
	public Float getScenicScore() {
		return scenicScore;
	}
	public void setScenicScore(Float scenicScore) {
		this.scenicScore = scenicScore;
	}
}
